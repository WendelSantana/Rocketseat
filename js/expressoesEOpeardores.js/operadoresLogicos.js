// Operadores lógicos (logical operators)

//  => 2 valores booleanos, quando verificados, resultará em verdadeiro ou falso.

let bread = true
let cheese = true

// AND(E) => && 
console.log(bread && cheese) // as duas condições precisam ser verdadeiro ou true

// OR(OU) => ||
console.log(bread || cheese) // uma das condições precisa ser verdadeira ou true

// Not(não) => !
console.log(!bread) // esse operador inverte a lógica da condição.
