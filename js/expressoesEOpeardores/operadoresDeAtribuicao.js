// Operadores de atribuição (assignment)
let x

// assignment
x = 2

// addition assignment
console.log(x += 2) // x = x + 2

// subtraction assignment
console.log(x-= 1) // x = x - 1

// multiplication assignment
console.log(x *= 1) // x = x * 1

// division assignment
console.log(x /= 1) // x = x / 1

// remainder, exponentiation
console.log(x %= 1) // x = x % 1
console.log(x **= 2) // x = x ** 2
